#include "libft.h"

void	freetab(void **tab)
{
	int	i;

	if (*tab && tab)
	{
		i = 0;
		while (tab[i])
		{
			if (tab[i])
				free (tab[i]);
			i++;
		}
		free (tab);
	}
}
